package byui.cit260.theLastOfUs.model;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author iu
 */
public class Scene4 implements Serializable{
        private String name;
        private String description;
        private double travelTime;
        private boolean DisplaySymbol;

    public Scene4() {
    }
        
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getTravelTime() {
        return travelTime;
    }

    public void setTravelTime(double travelTime) {
        this.travelTime = travelTime;
    }

    public boolean isDisplaySymbol() {
        return DisplaySymbol;
    }

    public void setDisplaySymbol(boolean DisplaySymbol) {
        this.DisplaySymbol = DisplaySymbol;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 59 * hash + Objects.hashCode(this.name);
        hash = 59 * hash + Objects.hashCode(this.description);
        hash = 59 * hash + (int) (Double.doubleToLongBits(this.travelTime) ^ (Double.doubleToLongBits(this.travelTime) >>> 32));
        hash = 59 * hash + (this.DisplaySymbol ? 1 : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Scene4 other = (Scene4) obj;
        if (Double.doubleToLongBits(this.travelTime) != Double.doubleToLongBits(other.travelTime)) {
            return false;
        }
        if (this.DisplaySymbol != other.DisplaySymbol) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.description, other.description)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Scene4{" + "name=" + name + ", description=" + description + ", travelTime=" + travelTime + ", DisplaySymbol=" + DisplaySymbol + '}';
    }
        
        
}
